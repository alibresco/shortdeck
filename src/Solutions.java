  private static int findFirst(HandType handType, List<Range> ranges) {
    for (int i = 19; i >= 0; i--) {
      if (ranges.get(i).getTypes().contains(handType)) {
        return i + 1;
      }
    }
    return 0;
  }

  private static String makeNumString(int num) {
    if (num == 20) {
      return "20\\+";
    } else if (num > 9) {
      return num + " ";
    } else {
      return " " + num + " ";
    }
  }

  private static String addKey(String str) {
    StringBuilder out = new StringBuilder("    A   K   Q   J   T   9   8   7   6  \n");
    List<String> ranks = Stream.of(Rank.values()).map(Rank::toString).collect(Collectors.toList());
    Collections.reverse(ranks);
    List<String> split = Arrays.asList(str.split("\\n"));
    for (int i = 0; i < split.size(); i++) {
      if (i % 2 == 0) {
        out.append(" ");
      } else {
        out.append(ranks.get(i / 2));
      }
      out.append(" ").append(split.get(i)).append("\n");
    }
    return out.toString();
  }

  public static void main(String[] args) {

    String shoveOut = Range.ALL.toPrettyString();
    String callOut = Range.ALL.toPrettyString();
    for (HandType type : Range.ALL.getTypes()) {
      String shove = makeNumString(findFirst(type, SHOVES));
      String call = makeNumString(findFirst(type, CALLS));
      shoveOut = shoveOut.replaceAll(type.toString() + " ?", shove);
      callOut = callOut.replaceAll(type.toString() + " ?", call);
    }
    shoveOut = addKey(shoveOut);
    callOut = addKey(callOut);
    System.out.println("Shoving range:");
    System.out.println(shoveOut);
    System.out.println("Calling range:");
    System.out.println(callOut);
    System.out.println(Range.ALL.toPrettyString());
    System.out.println(new Range("AKs,AKo,AA").toPrettyString());
  }
